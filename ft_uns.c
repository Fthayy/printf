#include "ft_printf.h"

int	ft_uns(unsigned int x)
{
	int res;

	res = 0;
	if (x >= 0 && x <= 9)
		res += ft_putchr(x + '0');
	else
	{
		ft_uns(x / 10);
		ft_uns(x % 10);
	}
	return (res);
}
